import math

# 1. Penjumlahan
def tambah(bil1, bil2):
    hasil = bil1 + bil2
    print("Hasil Penjumlahan:", hasil)

# 2. Pengurangan
def kurang(b1,b2):
    hasil = b1 - b2
    print("Hasil Pengurangan:", hasil)

# 3. Perkalian
def kali(b1,b2):
    hasil = b1 * b2
    print("Hasil Perkalian:", hasil)

# 4. Pembagian
def bagi(b1,b2):
    hasil = b1 / b2
    print("Hasil Pembagian:", hasil)

# 5. Pangkat
def pangkat(b1,b2):
    hasil = math.pow(b1,b2)
    print("Hasil Pangkat:", hasil)

# 6. Sin
def sin(b1):
    hasil = math.sin(b1)
    print("Hasil Sin:", hasil)

# 7. Cos
def cos(b1):
    hasil = math.cos(b1)
    print("Hasil Cos:", hasil)

# 8. Tan
def tan(b1):
    hasil = math.tan(b1)
    print("Hasil Tan:", hasil)

# 9. Log
def log(b1):
    hasil = math.log(b1)
    print("Hasil Log:", hasil)

# 10. Akar
def akar(b1):
    hasil = math.sqrt(b1)
    print("Hasil Akar:", hasil)
